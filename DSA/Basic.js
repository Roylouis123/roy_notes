// Data Structures are ways to organize and store data so that it can be accessed and modified efficiently. Algorithms are step-by-step procedures or formulas for solving a problem. Together, DSA forms the backbone of computer science, enabling efficient data handling, manipulation, and storage.


// Types of Data Structures
// Data structures are broadly categorized into two types:


////// Linear Data Structures
////// Non-Linear Data Structures



// ========================Linear Data Structures==============================

// In linear data structures, data elements are arranged sequentially or linearly,
//  where each element is connected to its previous and next element.



// -------Arrays:

// A collection of elements identified by index or key.
// Fixed size and elements are stored in contiguous memory locations.
// Example: int arr[5] = {1, 2, 3, 4, 5};



// ------Linked Lists:

// A sequence of elements where each element points to the next one.
// Elements are called nodes, and each node contains data and a reference to the next node.
// Types: Singly Linked List, Doubly Linked List, Circular Linked List.



// -------Stacks:

// LIFO (Last In, First Out) data structure.
// Example: stack.push(10); stack.pop();



// ------Queues:

// FIFO (First In, First Out) data structure.
// Example: queue.enqueue(10); queue.dequeue();



// ------Strings:

// A sequence of characters.
// Example: let str = "hello";








// Non-Linear Data Structures
// In non-linear data structures, data elements are not arranged sequentially. Instead, they are arranged in a hierarchical manner.



// ------Trees:

// A hierarchical structure with a root node and children nodes.
// Types: Binary Tree, Binary Search Tree, AVL Tree, B-tree, etc.




// ------Graphs:

// A collection of nodes (vertices) connected by edges.
// Types: Directed Graph (Di-Graph), Undirected Graph, Weighted Graph, Unweighted Graph.




// ------Heaps:

// A special tree-based data structure that satisfies the heap property.
// Types: Max-Heap, Min-Heap.



// ------Hash Tables:

// Stores data in an associative manner.
// Uses a hash function to compute an index into an array of buckets or slots.




// Summary
// Linear Data Structures: Arrays, Linked Lists, Stacks, Queues, Strings.
// Non-Linear Data Structures: Trees, Graphs, Heaps, Hash Tables.